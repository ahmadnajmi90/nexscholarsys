<?php

namespace App\Providers;

use Illuminate\Auth\Events\Registered;
use Illuminate\Auth\Listeners\SendEmailVerificationNotification;
use Illuminate\Foundation\Support\Providers\EventServiceProvider as ServiceProvider;
use Illuminate\Support\Facades\Event;
use App\Events\ConnectionStatusChanged;
use App\Listeners\SendConnectionNotification;
use Illuminate\Notifications\DatabaseNotification;
use App\Observers\DatabaseNotificationObserver;

class EventServiceProvider extends ServiceProvider
{
    /**
     * The event to listener mappings for the application.
     *
     * @var array<class-string, array<int, class-string>>
     */
    protected $listen = [
        Registered::class => [
            SendEmailVerificationNotification::class,
        ],
        ConnectionStatusChanged::class => [
            SendConnectionNotification::class,
        ],
        \App\Events\ProfileDataChanged::class => [
            \App\Listeners\InvalidateUserInsights::class,
            \App\Listeners\RegenerateUserEmbedding::class,
        ],
    ];

    /**
     * Register services.
     */
    public function register(): void
    {
        //
    }

    /**
     * Bootstrap services.
     */
    public function boot(): void
    {
        // Register the DatabaseNotification observer
        DatabaseNotification::observe(DatabaseNotificationObserver::class);
    }

    /**
     * Determine if events and listeners should be automatically discovered.
     */
    public function shouldDiscoverEvents(): bool
    {
        return false;
    }
}
