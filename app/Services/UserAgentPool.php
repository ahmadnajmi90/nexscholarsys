    /**
     * Get a random user agent from the pool
     * 
     * @return string
     */
    public static function getRandom(): string
    {
        return self::$userAgents[array_rand(self::$userAgents)];
    }
    
    /**
     * Get all user agents
     * 
     * @return array
     */
    public static function getAll(): array
    {
