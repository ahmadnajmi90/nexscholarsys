export const NavbarButton = ({
  href,
  children,
  className,
  variant = "primary",
  onClick,
}) => {
  const baseStyles =
    "px-6 py-3 rounded-lg text-base font-semibold transition-all duration-200 cursor-pointer inline-block text-center";

  const variantStyles = {
    primary: "bg-white text-black border border-gray-200 hover:bg-gray-50 shadow-sm",
    secondary: "bg-transparent text-gray-600 hover:text-black border border-gray-300 hover:bg-gray-50",
    gradient: "bg-gradient-to-r from-purple-600 to-pink-600 text-white hover:from-purple-700 hover:to-pink-700 shadow-lg",
  };

  const Component = href ? "a" : "button";

  return (
    <Component
      href={href}
      onClick={onClick}
      className={cn(baseStyles, variantStyles[variant], className)}
    >
      {children}
    </Component>
  );
};
